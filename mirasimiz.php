                <p>Lüleburgaz Sokullu Mehmet Paşa Külliyesi, Lüleburgaz ilçe girişinde oldukça geniş bir sahaya yayılmıştır.1569-1570 (H.977) yıllarında ibadet, ticaret ve eğitim amaçlı yapılan külliye; cami, kemerli dükkanları, hanı, hamamı, medresesi ile bir kompleks özelliği göstermektedir. Ancak çeşitli tahribatlara maruz kalan külliye, bugün adeta birbirinden bağımsız birer yapı görünümü arz etmektedir. 16'ncı yüzyılın tanınmış devlet adamı, devşirme kökenli, saray damadı, 1505 yılı doğumlu, saraydaki çeşitli görevlerini takiben kaptan-ı deryalık, sancak beyliği, Rumeli Beylerbeyliği ve vezirlik görevlerinde bulunmuş, 1564 yılında sadrazam olmuş ve bu görevini ölüm tarihi olan 1579 yılına kadar sürdürmüş olan Sokullu Mehmet Paşa tarafından bu külliyenin 1569 yılında Lüleburgaz’da yaptırılma amacı; Osmanlı’nın 16'ncı yüzyıldaki askeri, siyasi, ticari, sosyal, haberleşme ve ulaşım politikasının bir gereği olarak düşünülebilir. Şöyle ki; Osmanlı İmparatorluğu'nda Kanuni Sultan Süleyman (1520-1566) ile birlikte Balkanlara yapılan seferlerin artması, eski bir Roma yolu olan ve Lüleburgaz’ın da içinden geçen İstanbul-Edirne-Belgrad yolunun önemini arttırmış, bu güzergâhın iyileştirilmesini gerektirmiştir. Dolayısıyla bu yol üzerinde bulunan her önemli mevki, menzil adı verilen durak ve konaklama yeri olarak belirlenmiştir. Lüleburgaz, bu dönem ile birlikte ordunun konaklayabileceği, iaşesini sağlayabileceği menziller içinde yer almaktaydı. İstanbul ve Trakya bölgesi Balkan şehirleri arasında buğday, tahıl, canlı hayvan, kumaş vb. malları taşıyan kervanların konaklayacağı tesislere olan gereksinim, çevresi zengin tarım alanları ve meralarla çevrili Lüleburgaz’da büyük bir kervansaray, çok sayıda alış-veriş dükkanı ve panayır (Evliya Çelebi burada kurulan panayıra “Sığır Panayırı” der.) kurulmasını gerektirmiştir.</p>
